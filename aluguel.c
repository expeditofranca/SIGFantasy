char modulo_aluguel(void) {
    char opcao_c;
    do {
        opcao_c = menu_aluguel();
        switch(opcao_c) {
            case '1': cadastrar_aluguel();
                      break;
            case '2': atualizar_aluguel();
                      break;
            case '3': excluir_aluguel();
                      break;
            case '4': pesquisar_aluguel();
                      break;
        }
    } while(opcao_c != '0');
    return 0;
}

